package oop1;

public class IndividualCustomer extends Customer {       //IndividualCustomer = bireysel müşteridir ve oda bir müşteridir "extends" 
     private String firstName;
     private String lastName;                         //IndividualCustomer get ve set haline getiri  ldi
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
                                                       
     
     
	
     
     
     
	
}
